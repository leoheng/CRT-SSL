<h1>常见问题</h1>

<h2>该服务是否免费？</h2>
<p>该服务永久免费，不过如果您添加了特别多的域名，我们可能会联系您作额外的安排。</p><br>

<h2>证书过期检测的频率如何？</h2>
<p>我们至少每两天检测一次；通常情况下我们每天检测一次。</p><br>

<h2>我何时会收到邮件通知？</h2>
<p>我们将在以下的时间节点通知您：<br>
<ul>
  <li>当您注册的时候以便确认您的域名；</li>
  <li>当您的证书将在以下的时间段后过期：</li>
  <ul>
    <li>90 天 (3个月)</li>
    <li>60 天 (2个月)</li>
    <li>30 天 (1个月)</li>
    <li>14 天 (2 周)</li>
    <li>7 天 (1 周)</li>
    <li>5 天</li>
    <li>3 天</li>
    <li>2 天</li>
    <li>1 天</li>
  </ul>
  <li>您证书过期的当天；</li>
  <li>您的证书过期两天以后且仍未更新证书；</li>
  <li>您的证书过期七天以后且仍未更新证书；</li>
  <li>您的站点无法访问；</li>
  <li>您的站点连续七天无法访问，此后我们将不再检测您的网站的证书。</li>
</ul>
  如果您在到期前更换/续期了您的证书，我们将停止给您发送邮件，直到您新的证书过期。<br>
  我们绝对不会给您发送垃圾邮件或者将您的信息售卖给第三方。<br>
  </p><br>


<h2>该服务是否检测证书链中的全部证书？</h2>
<p>是的，我们会检测证书链中的全部证书，最多检测10级证书链。如果链中的某个证书即将过期，您也会收到关于该证书的过期提醒。</p><br>

<h2>你们是否有在线率保证？</h2>
<p>我们尽最大努力提供最好的服务。本项目完全开源，如果您需要100%的在线率保证，可以自行搭建该程序。</p><br>

<h2>该项目基于何证书？</h2>
<p>GNU Affero GPL v3 或更新版本.</p><br>

